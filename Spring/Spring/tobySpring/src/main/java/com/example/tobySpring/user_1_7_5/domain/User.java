package com.example.tobySpring.user_1_7_5.domain;

public class User {

	String id;
	String name;
	String password;

	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getPassword() {
		return password;
	}
	public void setId(String id) {
		this.id = this.id;
	}
	public void setName(String name) {
		this.name = this.name;
	}
	public void setPassword(String password) {
		this.password = this.password;
	}
}